class Solution {
  public:
    // Function to find two repeated elements.
    vector<int> twoRepeated(int n, int arr[]) {
        // Your code here
        unordered_map<int,int>mpp;
        vector<int>v;
        for(int i=0;i<n+2;i++){
            mpp[arr[i]]++;
            if(mpp[arr[i]]==2){
                v.push_back(arr[i]);
            }
        }
        return v;
    }
};

